package day_19_Loop;

public class Letters {
    public static void main(String[] args) {
        for (int i = 65; i <=90 ; i++) {
            System.out.print((char)i+ " ");
        }
        System.out.println();
        System.out.println("------------------------------------------------------------------------------");

        for (char i = 'A'; i <='Z' ; i++) {
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("-------------------------------------------------------------------------------");
        for (char i = 'Z'; i >='A' ; i--) {
            System.out.print(i+" ");
        }
        System.out.println("--------------------------------------------------------------------------------");
        for (char i= 10; i <=30 ; i++) {
            System.out.print(i+" ");
        }



















    }

}
