package day_03_Variables;

// Variable: Makes data reusable
// Buhel too :byte, short, int (more preferred), long
// Zadgai too float, double(more preferred)
public class VariableIntro {
    public static void main(String[] args) {
        byte myAge = 23;
        byte myWifeAge = 22;


        System.out.print(myAge);
        System.out.println(myWifeAge);


        int salary = 10000000;

        long money = 999999999999l;
        System.out.println(money);
        System.out.println(salary);
        System.out.print("My salary is ");
        System.out.print(salary);



    }

}
